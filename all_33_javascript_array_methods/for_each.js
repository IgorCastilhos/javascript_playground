// O método Array.forEach() executa uma dada função em cada elemento de um array.
const array = ["banana", "maçã", "uva"];
array.forEach((callbackFunction) => console.log(callbackFunction));

// Quando usar um forEach?
// Quando tu quer executar a mesma função callback em todos os elementos de um array.

const food = ['apple', 'carrot', 'pear', 'eggplant', 'banana', 'tomato', 'potato', 'avocado'];
food.forEach(item => console.log(item));